export const ALL_LINES = [...SUBWAY_LINES, ...COMMUTER_RAIL_LINES];

// Helper function to get all stations from selected lines
export function getStationsFromLines(selectedLineIds: string[]): string[] {
  if (selectedLineIds.length === 0) {
    return [];
  }

  const stationSet = new Set<string>();

  ALL_LINES.forEach((line) => {
    if (selectedLineIds.includes(line.id)) {
      line.stations.forEach((station) => stationSet.add(station));
    }
  });

  return Array.from(stationSet).sort();
}

// Helper function to get all subway stations
export function getAllSubwayStations(): string[] {
  const stationSet = new Set<string>();
  SUBWAY_LINES.forEach((line) => {
    line.stations.forEach((station) => stationSet.add(station));
  });
  return Array.from(stationSet).sort();
}

// Helper function to get all commuter rail stations
